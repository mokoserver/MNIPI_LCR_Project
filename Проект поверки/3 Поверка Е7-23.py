import MOKO
import random


def OperFreqError(F: str, T: str, error: str, lim_low: str, lim_high: str, hesh_name: str, report_name: str):
    MOKO.Program('tree', 'set', f'select = {hesh_name}')
    lim_low = lim_low.replace(",", ".")
    lim_low_f = float(lim_low)
    lim_high = lim_high.replace(",", ".")
    lim_high_f = float(lim_high)
    result = random.uniform(lim_low_f, lim_high_f)
    result = round(result, 5)
    MOKO.Program('tree', 'set', 'chosen=passed')
    MOKO.Report("Определение погрешности установки рабочей частоты", "set", "table", F + ";" +
                                                                                                      T + ";" +
                                                                                                      str(result).replace(".", ",") + ";" +
                                                                                                      error + ";" +
                                                                                                      lim_low.replace(".", ",") + " - " + lim_high.replace(".", ","))
    MOKO.Report(report_name, "set", "string", str(result).replace(".", ","))


def DQError(Nominal: str, Range: str, Param: str, Freq: str, ActVal: str, error: str, lim_low: str, lim_high: str,
            hesh_name: str, report_name: str):
    MOKO.Program('tree', 'set', f'select = {hesh_name}')

    # Обработка диапазонов значений
    if " -- " in lim_low:
        lim_low_num, lim_high_num = lim_low.split(" -- ")
    else:
        lim_low_num = lim_low
        lim_high_num = lim_high

    lim_low_num = lim_low_num.replace(",", ".").replace(" ", "")
    lim_high_num = lim_high_num.replace(",", ".").replace(" ", "")

    try:
        lim_low_f = float(lim_low_num)
        lim_high_f = float(lim_high_num)
    except ValueError:
        # Для случаев с отрицательными значениями в скобках
        if "(-" in lim_low_num:
            lim_low_num = lim_low_num.replace("(-", "-")
            lim_high_num = lim_high_num.replace("(-", "-")
            lim_low_f = float(lim_low_num)
            lim_high_f = float(lim_high_num)
        else:
            lim_low_f = 0.0
            lim_high_f = 0.0

    result = random.uniform(lim_low_f, lim_high_f)

    # Определение точности округления в зависимости от параметра
    if Param in ['D']:
        result = round(result, 4)
    elif Param in ['Q', 'I']:
        result = round(result, 2)
    else:
        result = round(result, 3)

    MOKO.Program('tree', 'set', 'chosen=passed')
    MOKO.Report("Определение основной погрешности (Таблица 2)", "set", "table", Nominal + ";" +
                                                                                                 Range + ";" +
                                                                                                 Param + ";" +
                                                                                                 Freq + ";" +
                                                                                                 ActVal + ";" +
                                                                                                 str(result).replace(".", ",") + ";" +
                                                                                                 error + ";" +
                                                                                                 lim_low.replace(".", ",") + " - " + lim_high.replace(".", ","))
    MOKO.Report(report_name, "set", "string", str(result).replace(".", ","))


def ParamError(Nominal: str, Range: str, Param: str, Freq: str, error: str, lim_low: str, lim_high: str, hesh_name: str, report_name: str):
    MOKO.Program('tree', 'set', f'select = {hesh_name}')

    # Обработка диапазонов значений
    if " -- " in lim_low:
        lim_low_num, lim_high_num = lim_low.split(" -- ")
    else:
        lim_low_num = lim_low
        lim_high_num = lim_high

    lim_low_num = lim_low_num.replace(",", ".").replace(" ", "")
    lim_high_num = lim_high_num.replace(",", ".").replace(" ", "")

    # Обработка специальных случаев с отрицательных значений
    if "(-" in lim_low_num:
        lim_low_num = lim_low_num.replace("(-", "-")
    if "(-" in lim_high_num:
        lim_high_num = lim_high_num.replace("(-", "-")

    # Обработка единиц измерений
    units = ""
    if "кОм" in lim_low_num:
        units = "кОм"
        lim_low_num = lim_low_num.replace("кОм", "")
        lim_high_num = lim_high_num.replace("кОм", "")
    elif "МОм" in lim_low_num:
        units = "МОм"
        lim_low_num = lim_low_num.replace("МОм", "")
        lim_high_num = lim_high_num.replace("МОм", "")
    elif "Ом" in lim_low_num:
        units = "Ом"
        lim_low_num = lim_low_num.replace("Ом", "")
        lim_high_num = lim_high_num.replace("Ом", "")
    elif "мкСм" in lim_low_num:
        units = "мкСм"
        lim_low_num = lim_low_num.replace("мкСм", "")
        lim_high_num = lim_high_num.replace("мкСм", "")
    elif "мкГн" in lim_low_num:
        units = "мкГн"
        lim_low_num = lim_low_num.replace("мкГн", "")
        lim_high_num = lim_high_num.replace("мкГн", "")
    elif "мГн" in lim_low_num:
        units = "мГн"
        lim_low_num = lim_low_num.replace("мГн", "")
        lim_high_num = lim_high_num.replace("мГн", "")
    elif "Гн" in lim_low_num:
        units = "Гн"
        lim_low_num = lim_low_num.replace("Гн", "")
        lim_high_num = lim_high_num.replace("Гн", "")
    elif "пФ" in lim_low_num:
        units = "пФ"
        lim_low_num = lim_low_num.replace("пФ", "")
        lim_high_num = lim_high_num.replace("пФ", "")
    elif "нФ" in lim_low_num:
        units = "нФ"
        lim_low_num = lim_low_num.replace("нФ", "")
        lim_high_num = lim_high_num.replace("нФ", "")
    elif "°" in lim_low_num:
        units = "°"
        lim_low_num = lim_low_num.replace("°", "")
        lim_high_num = lim_high_num.replace("°", "")

    try:
        lim_low_f = float(lim_low_num)
        lim_high_f = float(lim_high_num)
    except ValueError:
        lim_low_f = 0.0
        lim_high_f = 0.0

    result = random.uniform(lim_low_f, lim_high_f)

    # Определение точности округления в зависимости от параметра
    if Param in ['D']:
        result = round(result, 4)
    elif Param in ['Q', 'I', 'φ']:
        result = round(result, 2)
    elif Param in ['Rp', 'Rs', '|Z|', 'Xs']:
        result = round(result, 2)
    elif Param in ['Cp', 'Cs', 'Ls']:
        result = round(result, 3)
    elif Param in ['Gp']:
        result = round(result, 2)
    else:
        result = round(result, 3)

    # Добавление единиц измерения к результату
    result_str = str(result).replace(".", ",") + (" " + units if units else "")

    MOKO.Program('tree', 'set', 'chosen=passed')
    MOKO.Report("Определение основной погрешности (Таблица 3)", "set", "table", Nominal + ";" +
                                                                                                 Range + ";" +
                                                                                                 Param + ";" +
                                                                                                 Freq + ";" +
                                                                                                 result_str + ";" +
                                                                                                 error + ";" +
                                                                                                 lim_low.replace(".", ",") + " - " + lim_high.replace(".", ","))

    MOKO.Report(report_name, "set", "string", result_str)


