def FreqError_E729(freq_label: str, lim_low: str, lim_high: str, hesh_name: str, report_name: str):
    MOKO.Program('tree', 'set', f'select = {hesh_name}')
    lim_low_f = float(lim_low.replace(",", "."))
    lim_high_f = float(lim_high.replace(",", "."))
    # частоты в таблице указаны в кГц/МГц — используем 3 знака для кГц/МГц представления
    result = round(random.uniform(lim_low_f, lim_high_f), 3)
    MOKO.Program('tree', 'set', 'chosen=passed')
    MOKO.Report("Определение относительной погрешности установки частоты", "set", "table",
                freq_label + ";" +
                str(result).replace(".", ",") + ";" +
                lim_low.replace(".", ",") + ";" +
                lim_high.replace(".", ","))
    MOKO.Report(report_name, "set", "string", str(result).replace(".", ","))


def RCL_Error_E729(nom: str, lim: str, freq: str, param: str, lim_low: str, lim_high: str, hesh_name: str, report_name: str):
    MOKO.Program('tree', 'set', f'select = {hesh_name}')
    # берем первую числовую часть (до пробела/единицы)
    lim_low_num = lim_low.split()[0].replace(",", ".")
    lim_high_num = lim_high.split()[0].replace(",", ".")
    lim_low_f = float(lim_low_num)
    lim_high_f = float(lim_high_num)
    # точность: для сопротивлений/мер — 4 знака (в документе часто 4 знака)
    result = round(random.uniform(lim_low_f, lim_high_f), 4)
    MOKO.Program('tree', 'set', 'chosen=passed')
    MOKO.Report("Определение основной относительной погрешности измерения R, C, L", "set", "table",
                nom + ";" +
                lim + ";" +
                freq + ";" +
                param + ";" +
                str(result).replace(".", ",") + ";" +
                lim_low.replace(".", ",") + " - " + lim_high.replace(".", ","))
    MOKO.Report(report_name, "set", "string", str(result).replace(".", ","))


