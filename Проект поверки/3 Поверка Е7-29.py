def DQ_Error_E729(measure: str, param: str, act_val: str, lim_low: str, lim_high: str, hesh_name: str, report_name: str):
    MOKO.Program('tree', 'set', f'select = {hesh_name}')
    lim_low_f = float(lim_low.replace(",", "."))
    lim_high_f = float(lim_high.replace(",", "."))
    if param.lower() in ["d", "tgδ", "tgd", "tgδ(d)"]:
        result = round(random.uniform(lim_low_f, lim_high_f), 4)
    elif param.lower() == "q":
        result = round(random.uniform(lim_low_f, lim_high_f), 2)
    else:
        result = round(random.uniform(lim_low_f, lim_high_f), 4)

    MOKO.Program('tree', 'set', 'chosen=passed')


    MOKO.Report("Определение основной абсолютной погрешности D, Q", "set", "table", measure + ";" +
                                                                                                    param + ";" +
                                                                                                    act_val + ";" +
                                                                                                    str(result).replace(".", ",") + ";" +
                                                                                                    lim_low.replace(".", ",") + " - " + lim_high.replace(".", ","))

    MOKO.Report(report_name, "set", "string", str(result).replace(".", ","))


